// ---------- 공용 API 호출기 (v15.1) ----------
// 429(속도 제한)·500·529(과부하)는 지수 백오프로 최대 3회 재시도.
// 401(키 오류)·400(크레딧 부족 등)은 재시도해도 소용없으니 즉시 원인을 던진다.
async function callAnthropic(body) {
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) throw new Error("ANTHROPIC_API_KEY가 설정되지 않았습니다.");
  let lastErr;
  for (let attempt = 0; attempt < 3; attempt++) {
    let res;
    try {
      res = await fetch("https://api.anthropic.com/v1/messages", {
        method: "POST",
        headers: { "content-type": "application/json", "x-api-key": apiKey, "anthropic-version": "2023-06-01" },
        body: JSON.stringify(body),
      });
    } catch (e) {
      lastErr = new Error("네트워크 오류: " + e.message);
      await sleep(1500 * (attempt + 1));
      continue;
    }
    if (res.ok) {
      const data = await res.json();
      const text = (data.content || []).filter((b) => b.type === "text").map((b) => b.text).join("\n").trim();
      if (!text) throw new Error("빈 응답");
      return text;
    }
    const detail = await res.text();
    lastErr = new Error(`Anthropic API ${res.status}: ${detail.slice(0, 300)}`);
    if (![429, 500, 529].includes(res.status)) throw lastErr; // 401/403/400 등은 즉시 중단
    const retryAfter = Number(res.headers.get("retry-after")) || 0;
    await sleep(Math.max(retryAfter * 1000, 2500 * (attempt + 1)) + Math.random() * 1000);
  }
  throw lastErr;
}
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

