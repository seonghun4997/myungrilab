const nameize = (text, name) => text.replaceAll("{NAME}", name || "그대");

export async function generateChapter(facts, chapterId, name) {
  const text = await callAnthropic({
    model: MODEL,
    max_tokens: 3000,
    system: SYSTEM_V15,
    messages: [{ role: "user", content: `[고객 이름] ${name || "고객"}\n[명반 데이터]\n${JSON.stringify(facts, null, 1)}\n\n[작성 지시]\n${CHAPTER_PROMPTS[chapterId]}` }],
  });
  return nameize(text, name);
}
